public class EndsWithString {
    public static void main(String[] args) {
        String a = "Ujang Herlan";

        System.out.println("Hasilnya:"+a.endsWith("U"));
        System.out.println("Hasilnya:"+a.endsWith("e"));
        System.out.println("Hasilnya:"+a.endsWith("lan"));
        System.out.println("Hasilnya:"+a.endsWith("jang"));

    }
}
